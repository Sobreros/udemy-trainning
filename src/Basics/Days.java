package Basics;

public class Days {
	public static void main(String[] args) {
		
		String dayofweek = "Tuesday";
		
		switch (dayofweek) {
		case "Monday" :
			System.out.println("Today is Monday");
		break;	
		case "Tusday" :
			System.out.println("Today is Tusday");
		break;	
		case "Sat" :
			System.out.println("Today is Sat");
		break;
		
		}
	}

}
